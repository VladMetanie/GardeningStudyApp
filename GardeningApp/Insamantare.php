<div>
    <h2>Capitolul 1</h2>
    <br>
    <p> Semintele se gasesc in magazine de specialitate, magazine DIY, dar si online. De obicei acestea sunt soiuri selectionate si sunt atent verificate daca corespund standardelor in vigoare. Incercati sa aflati: perioada de valabilitate, soiul, capacitatea de germinare, procentaj de substante/soiuri straine.
        <br>
        <br>

        Dupa ce cumparati semintele: depozitati-le intr-un loc potrivit: uscat, racoros, fara lumina (magazia de unelte, garajul), altfel isi vor pierde capacitatea de germinare.
        <br>
        Producatorii de seminte de gazon au combinatii gata facute pentru diferite medii si conditii, dintre care poti alege. Ambalajele specifica de obicei si perioada de insamantare, dar si suprafata pe care o acopera.
        
    
        
    </p>
    <br>
    <p> 
        Mai departe vom discuta despre germinare. Germinatia este un proces fiziologic de trecere a unui germen de la viata latenta la viata activa si care duce la formarea unei noi plante sau a unui organ vegetal. Germinarea semintelor de iarba nu este influentata doar de cantitatea de apa si temperatura, ci si de de conditiile de luminozitate si cantitatea de oxigen din sol.
        
        <p>
            -Apa este necesara pentru umflarea semintelor, de aceea se uda imediat dupa insamantare. Dupa insamantare solul trebuie mentinut umed. pentru a facilita incoltirea, dar nu exagerati pentru ca pamantul mereu umed contine putin aer, iar lipsa aerului duce la dezvoltarea anumitor soiuri de ciuperci care ataca firele de iarba deja incoltite.</p>
      <br>  
    <p>-Pe langa umiditate si aer, este nevoie si de anumite temperaturi pentru dezvoltarea optima a gazonului:  temperatura minima trebuie sa fie 5degC, maxim 25degC, iar optimul este 16-21degC. De aceea cele mai multe soiuri de gazon se insamanteaza primavarea sau toamna devreme.</p>
    <br>
    <p>-Cele mai multe soiuri de iarba incoltesc in prezenta luminii - umbra  permanenta nu este un foarte bun prieten al gazonului. Pentru a le proteja de vant si apa semintele vor fi doar usor ingropate in pamant (aprox 5 mm) - o grebla este de ajutor in acest caz. Ingropate prea adanc, rasar mai greu.</p>
    <br>
    <p>-Daca firele de iarba nu au aceeasi desime peste tot, nu este un motiv de ingrijorare, pentru ca mai tarziu incep sa se inmulteasca radacinile si lujerii, formandu-se un adevarat covor de iarba. In zonele in care gazonul este extrem de rar, se va proceda la a doua insamantare pentru a se evita aparitia buruienilor.</p>
        
    <h2> Capitolul 2</h2>
    <p>
        In acest curs vom prezenta care sunt pasii de urmat pentru insamantarea gazonului
	<h3>
        1. Inlaturarea gazonului vechi/ingalbenit sau a buruienilor si afanarea solului</h3>
<p>Ramasitele gazonului vechi, pietrele mari si radacinile se scot cu harletul / lopata si cu grebla si se indeparteaza.
Daca este vorba despre un teren foarte buruienos, acesta se erbicideaza si apoi se sapa si se indeparteaza radacinile si pietrele. Pentru ierbicid: cereti sfatul unui specilist sau interesati-va la fitofarmacii despre cea mai buna solutie. Dupa anumite ierbicide nu este recomandat sa se planteze nimic circa 3-4 saptamani.
Inainte de plantare, pamantul trebuie sa fie bine afanat si fara bulgari mari sau pietre.</p>
<br>

<h3>2.	Ameliorarea solului </h3>
<p>
Inainte de a semana noul gazon, stratul superior al solului poate fi ameliorat, adica imbunatatit. Capacitatea de drenare a solurilor dure si dense se poate creste cu nisip cu graunte de 0.2-0.3 mm. Procentajul optim de nisip trebuie stabillit cu un specialist, pentru a nu afecta viitorul gazon.
Pamantul cu pietre si pietris este si el dificil de cultivat, pentru ca prima ploaie spala solul fertil si apoi survin lipsa de nutrienti si uscaciunea. Pe un astfel de teren este indicat sa se aduca un aport de pamant fertil de gradina. Un amestec de pamant de gradina, compost si nisip de 1:1:1 este ideal.
In mod ideal, planificarea gradinii incepe odata cu proiectarea casei, astfel incat inca de la saparea fundatiei, stratul de pamant fertil de la suprafata se va trata diferit: va fi depozitat intr-o movila separata si se vor semana pe el plante de tipul ingarasamntului verde pentru a-si pastra proprietatile si pentru a putea fi utilizat ulterior pentru gazon, gradina cu flori su gradina cu legume. Plantele ingrasamint verde sunt: trifoi, lucerna, in, lupin, mazare, mazariche, sulfina, rapita, mustarul, floarea soarelui.
</p>
<br>
<h3>3.	Aranjarea terenului
</h3>
<p>Pentru terenurile proaspat decopertate - precum cele din jurul unei constructii noi: acestea este indicat sa ramana o vreme mai indelungata neinsamantate, pentru a se da timp burieniler sa creasca.  Astfel acestea se pot identifica si smulge fara a afecta viitorul strat de iarba fina de gazon.
    Daca terenul este drept, este ideala crearea unei pante usoare de 2% (2 cm pe metru), pentru drenarea corespunzatoare, incepand de langa casa. Se pot delimita si viitoarele alei betonate, cu scanduri si proptele de lemn, care astfel ajuta si la delimitarea terenului de insamantat.</p>
<br>
<h3>    4.    Pregatirea terenului
</h3>
<p>Pregatirea terenului incepe cu saparea sau ararea acestuia. Eventualii bulgari trebuie sfaramati, pietrele indepartate si se vor grebla buruienile, radacinile si alte resturi.
</p>
<br>

<h3>5.   Semanare si acoperire
</h3>
<p>In general este necesara o contitate aproximativa de 20-30 grame de seminte pe metru patrat, dar respectati indicatiile semintelor pe care le achizitionati dvs.
    Cand se seamana cu semanatoare, semintele vor fi impartite in 2 iar terenul va fi parcurs de 2 ori: odata pe verticala si o data pe orizontala. Fasiile sa se suprapuna pe circa 10 cm.
    In cazul semanarii manuale, semintele se impart in 4 si se imprastie in sensurile din imaginea de mai jos. Dupa semanare semintele se ingroapa usor cu ajutorul unei greble.\
</p>
<br>
<h3>6. Tavalugire si udare
</h3>
<p>Pentru tavalugirea terenului se trece cu ruloul de tasat (rulou cu gratar, rulou de plastic umplut cu apa, rulou din lemn) peste pamant, pentru ca semintele si pamantul sa intre bine in contact - important pentru germinare. In timpul tavalugirii pamantul trebuie sa fie uscat.
    Prima udare se face dupa insamantare, iar ulterior umiditatea trebuie mentinuta relativ constanta. Cand este foarte cald afara se recomanda udari scurte si dese.
</p>
<br>

<h3>
    7.  Prima tundere

</h3>
<p>Prima tundere se face cand firele de iarba au ajuns la circa 10 cm. Se vor taia doar varfurile (1-2 cm), incurajandu-se astfel dezvoltarea radacinilor.
    In decursul urmatoarelor tunderi, iarba va fi scurtata din ce in ce mai mult, treptat ajungandu-se la o inaltime a firului de 3-4 cm, asa cum este firesc.
</p>
<br>
<h3>8.      Aerarea gazonului
</h3>
<p>Aerarea gazonului trebuie facuta incepand cu anul urmator plantarii, mai ales in zonele circulate intens, unde compactarea este evidenta si consta in introducerea unor tije solide in suprafata gazonului la o adancime de minim 7,5 cm: furca de gradina daca suprafata este mica sau un aparat special pentru aerat, pentru suprafete mari.
    In urma efectuarii acestei operatii are loc un schimb al aerului: oxigenul din aer, element vital pentru cresterea radacinii, ia locul dioxidului de carbon din sol. Ca beneficiu suplimentar si apa poate acum ajunge adanc la radacina si asta este important atat in timpul verii cat si iarna. Aceasta lucrare este ideal sa se faca toamna devreme, dar este bine si acum, primavara.
</p>
<br>

    </p>
    <h2> Capitolul 3</h2>
    <p> 
    <h3>In acest curs final vom prezenta tipurile de gazon folosite.</h3>

    <h3>Tipuri de gazon</h3>

    <p>Amestecul ideal de gazon pentru curtea ta tine cont de : </p>
       <p>-Expunerea terenului</p>
       <p>-Intrebuintarea ulterioara a gazonului</p>
        <p>-Cerinte de ingrijire a gazonului</p>
        <br>
    <p>Majoritatea tipurilor de gazon contin aceleasi soiuri de iarba de baza, rezistente la seceta, la umbra si la trafic (Festuca Arundinaceea, Poa pratensis si Lolium perenne), dar proportiile difera functie de destinatia terenului si expunerea lui. In aceeasi curte se pot semana doua sau mai multe tipuri de gazon, daca conditiile o cer.
    </p>
    <h3>Gazon pentru zone insorite</h3>

    <p>Este ideal pentru curtile expuse la lumina sudica, fara copaci sau alte surse de umbra.
    </p>
<p>Soiurile de iarba din compozitia lui il fac foarte rezistent la soarele puternic al verii si nu au nevoie de foarte multa apa pentru a arata bine. Asta dupa ce a ajuns la maturitate!
Soiurile de iarba care se afla de obicei in compozitia lui sunt Festuca Arundinaceea (40-60%), Poa pratensis si Lolium perenne.</p>

<br>
<h3>Gazon pentru zone umbroase:</h3>
<br>
<p>Se multumesc si doar cateva ore de soare pe zi, si rezista in colturile permanent umbrite ale gradinii, desi nu va fi foarte des acolo. Este ideal pentru curtile orientate spre nord sau la marginea unei liziere, unde umbra este persistenta si compacta si nu rezista foarte bine la trafic intens.
</p>
<br>
<p>Soiurile din componenta sa sunt de obicei: Festuca Tricopylla, Lolium perenne si Poa pratensis.
</p>
<br>
<h3>Gazon pentru zone semiumbrite:</h3>
<br>
<p>Este situatia mai tututor curtilor si gradinilor cu arbori: exista umbra, dar nu toata ziua si nu este foarte densa si exista soare macar 3-5 ore / zi. In compozitia sa intra tot Festuca Tricopylla, Lolium perenne si Poa pratensis, dar in proportii diferite si este vandut de obicei sub numele de gazon universal.
</p>
<br>
<h3>Gazon pentru trafic intens:</h3>
<br>
<p>Prin trafic intens se intelege: loc public putin intretinut, ocazional masini parcate si/sau animale de companie care isi fac nevoile, noxe - cum ar fi o intersectie aglomerata sau zonele verzi din orase in general. Soiurile de iarba din compozitia acestui tip de gazon au firul mai aspru, rezistent, lat si crestere puternica.
</p>
<p>Soiul de iarba care ii confera aceasta rezistenta este Lolium Perene si se combina de obicei cu Poa si Festuca.
</p>
<br>
<h3>Gazonul cu flori:
</h3>
<br>
<p>Daca semeni asa ceva nu obtii un gazon clasic, ci o pajiste inflorita, cu tulpini inalte, pe care odata ce le cosesti, vei obtine urmatoarea pajiste inflorita abia in anul urmator, exact asa cum se inampla in cazul fanetelor naturale.
</p>


