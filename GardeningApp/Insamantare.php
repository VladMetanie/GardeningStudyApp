    <h2>Introducere</h2>
    <br>
    <p>Un gazon frumos si sanatos se poate obtine prin 2 metode: prin instalarea de rulouri de gazon gata crescut sau prin insamantare gazon.  Prima metoda are avantajul rapiditatii, dar si costuri mai mari. Insamantarea gazonului este mai degraba pentru cei care nu se grabesc si mai ales le place ca lucrurile de pe langa casa sa fie facute pe indelete, de ei insisi. 
    </p>
    <br>
</div>
