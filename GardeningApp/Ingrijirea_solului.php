    <h2>Capitolul 3</h2>
    <p>Sapatul solului gradinii reprezinta o posibilitate grozava de a va elibera asa cum trebuie de toata agitatia saptamanii. Insa nu numai din acest punct de vedere este un lucru bun, ci si pentru ingrijirea solului.
    </p>
    <h3>Aici sunt 4 avantaje ale sapatului:</h3>
    <br>
    <p>1. Solul va fi afanat si aerisit.
        <br>Radacinile vor sa respire. Atunci cand solul in care sunt infipte radacinile se compacteaza, face cruste sau este apasat de atata calcat este impiedicat schimbul de gaze dintre radacina si suprafata si, pe termen lung, sanatatea radacinilor are de suferit. Si organismele utile din sol au nevoie de oxigen.
        <br>
        <br>Si vremea joaca un rol important. Din cauza ,,fortei" picaturilor de ploaie, suprafata solului isi pierde cu timpul structura fina granulara. Se formeaza o crusta.De aceea merita sa sapati pamantul din gradina! In acest mod crustele se rup, iar radacinile pot respira din nou in voie.

    </p>
    <br>
    <p>2. Vor fi indepartate buruienile
        <br>Buruienilor nu le place deloc sa fie deranjate atunci cand cresc. De aceea, sapatul este foarte eficient pentru prevenirea buruienilor.
        <br>
        <br>Problema este rezolvata din fasa: prin sapat, semintele de buruieni sunt distruse din timp. Chiar si buruienile perene, incapatanate, precum gura-leului, cedeaza in fata sapatului regulat. Prin aceasta, buruienile vor fi slabite in asa masura incat nu mai constituie o concurenta de luat in serios pentru plantele dorite.

    </p>
    <br>
    <p>3. Daunatorii sunt combatuti
        <br>Daca aveti probleme cu melcii, o sa va dezamagim la acest punct, din pacate. Sapatul nu este de prea mare ajutor. Insa, prin sapare, melcii sunt cel putin deranjati si nu isi vor depune neaparat ouale in bordurile dumneavoastra florale.
        Cartitele si soarecii-de-pamant nu sunt foarte incantati cand pamantul de deasupra lor este lucrat
    </p>
    <br>
    <p>4. Apa din sol se conserva
        <br>Sapatul protejeaza echilibrul hidric al solului. In solurile pe care s-a format crusta, apa de ploaie nu poate patrunde bine si, de aceea, se scurge nefolosita. In soluri afanate, apa de ploaie se infiltreaza cu usurinta - inclusiv in straturile mai adanci.
        <br>
        <br>In plus: atunci cand stratul de la suprafata are crusta, apa din sol poate urca de la adancime pana la suprafata. Se foloseste de spatiile fine dintre particulele de sol, asa-numitele capilare.In zilele calde, se pot pierde in acest mod pana la 6 litri pe zi pe metru patrat. Sapatul distruge capilarele din straturile superioare ale solului, astfel incat apa nu mai poate urca la suprafata si nu se mai poate evapora.
    </p>
