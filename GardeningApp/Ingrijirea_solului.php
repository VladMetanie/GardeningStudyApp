    <h2>Capitolul 1</h2>
    <p> Inainte sa analizam ingrijirea solului, ar trebui sa discutam mai intai despre sol propriu-zis. Intr-o mana de pamant exista mai multe vietati decat persoane pe Pamant. Ele pot realiza multe, daca le oferiti conditiile de viata potrivite. Vietatile din sol sunt clasificate in asa-numitele
        <br>
        <p>&nbsp&nbsp&nbsp&nbsp-microflora a solului (bacterii, ciuperci, alge)
        </p>
        <p>&nbsp&nbsp&nbsp&nbsp-fauna a solului (rame, nematozi, acarieni etc.).
        </p>
        
        <p>Vietatile din sol sunt responsabile cu maruntirea si transformarea substantelor organice moarte (frunze, radacini etc.). Pe acestea le transforma in humus nutritiv, ale carui substante nutritive sunt puse la dispozitie direct pentru plante si alte microorganisme. Sau, le transforma in humus permanent, care este descompus mai lent si, astfel, reprezinta o ,,sursa de substante nutritive" care dureaza mai mult.
            In plus, locuitorii solului amesteca solul si il afaneaza. Astfel, solul este ventilat si structura acestuia este imbunatatita. Sunt reduse problemele cu stagnarea umezelii in sol. Unele bacterii din sol pot fixa si utiliza azotul din aer, adesea in simbioza cu anumite plante.
            Rama este foarte harnica - aceasta transforma anual in humus de 70 de ori greutatea corpului sau.
            Mananca mai ales resturi proaspete de plante moarte, le transforma si le elimina din nou.
            Si ea ventileaza si afana solul si aduce la suprafata pamant si substante nutritive din straturile mai adanci de pamant.
        </p>
        

    </p>
    <br>
    <h3>Valoarea pH a solului</h3>
    <p>Valoarea pH va ofera informatii despre faptul ca solul dumneavoastra este acid, alcalin sau neutru. Un sol acid are o valoare pH sub 7, unul alcalin o valoare peste 7 si un sol neutru are o valoare pH de exact 7.
    <br>
    <br>
    Ar trebui sa stiti daca solul este acid sau nu, pentru ca valoarea pH corecta este extrem de importanta pentru unele plante.Insa cum aflati care este valoarea pH a solului dumneavoastra? In acest sens exista seturi de testare, care va ofera informatii rapid si sigure.
    
    
    </p> 
        
    
    <h2> Capitolul 2</h2>
    <p>
        Acum vom discuta despre activarea solului.Daca doriti sa ii oferiti sprijin solului din gradina, aveti cateva posibilitati de activare a acestuia:
        <br>
        <p> &nbsp&nbsp&nbsp&nbsp1)Utilizati regulat ingrasaminte organice cu eliberare lenta si praf de piatra si inglobati-le in sol numai superficial. Renuntati in cea mai mare masura la prelucrarea frecventa a solului, intensa si in profunzime.
        </p>
        <p> &nbsp&nbsp&nbsp&nbsp2)Utilizati produse fitofarmaceutice (chimice) numai in cazuri de necesitate absoluta.</p>
        <p> &nbsp&nbsp&nbsp&nbsp3)Rasfatati solul cu o incarcatura de compost in fiecare an. Cand produceti compost nou, cel mai bine utilizati un activator pentru compost. Pamantul cu o cota mare de compost din materiale verzi asigura, de asemenea, o vitalizare microbiana a solului.</p>
        <p> &nbsp&nbsp&nbsp&nbsp4)Mulciti regulat. Prin mulcire se intelege acoperirea uniforma a solului cu material verde taiat (mai ales deseuri de iarba). Atentie: nu aplicati un strat de mulci prea gros si evitati mulcirea in pozitii umbrite si umede, pentru ca se poate produce putrezire.
        </p>
        <p> &nbsp&nbsp&nbsp&nbsp5)Acordati atentie valorii pH a solului dumneavoastra si verificati-o regulat. In cazul unor valori pH prea reduse (sub 5,5) se recomanda o administrare de calcar si, ca masura preventiva, utilizarea unei ape potabile mai dure in loc de apa de ploaie.
        </p>
        <p> &nbsp&nbsp&nbsp&nbsp6)Bogate in nutrienti si deosebit de valoroase, pentru ca de obicei nu exista in circuitul din gradina, sunt composturile de origine animala, gen ingrasamintele organice provenite de la bovine.
        </p>
        
    </p>
    <p> Acum ar fi potrivit sa continuam pe tematica de activare a solului si sa discutam despre avantajele acestei activitati.
        Acesta este un principiu esential in agricultura biologica. Si, un sol activat, cu o viata sanatoasa a solului, prezinta urmatoarele avantaje:
        <br>
        <p>&nbsp&nbsp&nbsp&nbsp-Nu se produce mai deloc fertilizare excesiva si aciditate excesiva, inclusiv toate problemele aferente (dereglari de crestere si o incidenta marita a bolilor la plante)
        </p>
        <p>&nbsp&nbsp&nbsp&nbsp-Nu se produc deficite de substante nutritive, pentru ca este garantata o furnizare periodica de nutrienti.</p>
        <p>&nbsp&nbsp&nbsp&nbsp-Solul va fi afanat si sapat regulat, cvasi gratis si cu putine eforturi proprii.</p>
        <p>&nbsp&nbsp&nbsp&nbsp-Sunt imbunatatite atat capacitatea de retinere a apei (impotriva uscarii), cat si ventilarea solului (impotriva putrezirii).</p>
        <p>&nbsp&nbsp&nbsp&nbsp-Comparativ cu prelucrarea regulata a solului si cu fertilizarea minerala, volumul de munca este mai redus pe termen lung.
        </p>
        <p>&nbsp&nbsp&nbsp&nbsp-Cu compost, respectiv cu pamant fara turba sau cu o cantitate redusa de turba, veti aduce o contributie valoroasa la pastrarea mlastinilor, puternic periclitate.</p>
        <p>&nbsp&nbsp&nbsp&nbsp-Prin renuntarea la ingrasamintele minerale se reduce riscul de spalare si, astfel, o posibila poluare a apei freatice.</p>
        <p>Conditiile de sol extreme fac viata grea nu doar plantelor, ci si locuitorilor solului - circuitul din sol este perturbat.
In cazul in care solul este prea greu, argilos sau compactat:</p>
        <br>
        <p>&nbsp&nbsp&nbsp&nbspinglobati in el compost maturat sau pamant cu o cota mare de compost. Cu ajutorul materialului organic, particulele de argila se leaga in bulgari mici, intre care se formeaza spatii goale mai mari si, astfel, solul poate ,,respira" din nou.
        </p>
        <p>&nbsp&nbsp&nbsp&nbspImbogatiti solul cu nisip.</p>
        <p>&nbsp&nbsp&nbsp&nbspSapati solul, intorcandu-l inainte de primul ger si lasati bulgarii de pamant compactati expusi liber - gerul va rezolva restul prin ,,detonare"!
        </p>
        <p>&nbsp&nbsp&nbsp&nbspDaca solul este foarte compactat pana in straturile mai adanci, un drenaj este inevitabil.
        </p>
        <p>In cazul in care solul este prea usor, nisipos sau cu o retentie de apa prea redusa:</p>
        <br>
        <p>&nbsp&nbsp&nbsp&nbspinglobati in el compost sau pamant cu o cota mare de compost, precum si praf de roca primara sau ingrasaminte organice cu eliberare lenta, in vederea maririi continutului de humus al solului.</p>
        <p>Imbogatiti pamantul cu perlit horticol si vermiculit pentru a mari capacitatea solului de inmagazinare a apei.</p>
        <p>In cazul bordurilor florale este recomandata mulcirea sau acoperirea suprafetei cu lava vulcanica sau cu mulci de scoarta ornamentala, in vederea reducerii pierderilor de apa.</p>
    </p>
