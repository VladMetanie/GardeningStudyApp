    <h2> Capitolul 2</h2>
    <br>
    <h3>Tipuri de flori de gradina:</h3>
    <br>
    <h3>Flori de gradina sezoniere:
    </h3>
    <p>
        Florile de gradina sezoniere, cunoscute si sub denumirea de flori anuale, isi desfasoara ciclul de viata intr-un singur sezon (cald sau rece). Practic, dupa ce le plantezi, acestea cresc, infloresc, fac seminte si, in cele din urma, mor. Astfel, este necesar sa le plantezi din nou dupa sezonul de inflorire. Printre cele mai cunoscute tipuri de flori anuale se numara: 
        <br>
        <p> &nbsp&nbsp&nbsp&nbsp1)panselutele;
        </p>
        <p> &nbsp&nbsp&nbsp&nbsp2)galbenelele;</p>
        <p> &nbsp&nbsp&nbsp&nbsp3)albastrelele;</p>
        <p> &nbsp&nbsp&nbsp&nbsp4)gura-leului;</p>
        <p> &nbsp&nbsp&nbsp&nbsp5)craitele;</p>
        <p> &nbsp&nbsp&nbsp&nbsp6)carciumaresele</p>
        
    
    <p> Dintre acestea, carciumaresele sunt flori anuale rezistente, in sensul ca ele pot face fata conditiilor meteo nefavorabile (inghet), continua sa infloreasca si sa faca seminte si in anul urmator. Pe de alta parte, mor la scurt timp dupa a doua inflorire. Important de retinut e si faptul ca florile anuale, desi au o durata de viata mai scurta decat florile perene, sunt mai usor de ingrijit si au preturi mai avantajoase. Mai mult decat atat, acestea au un proces de crestere foarte rapid, astfel ca te vei putea bucura de frumusetea lor in cel mai scurt timp, mai ales ca ele alcatuiesc o paleta cromatica de vis. Totodata, florile sezoniere sunt o alegere excelenta chiar si pentru gradinile de mici dimensiuni.</p>
        <br>
        <h3>Flori de gradina perene: </h3>
        <br>
    <p>Spre deosebire de florile de gradina sezoniere (anuale), florile de gradina perene traiesc mai mult de doi ani consecutivi. Practic, radacinile acestora nu mor dupa etapa de inflorire, nici macar in conditii de inghet. Este important de retinut ca florile de gradina perene au totusi o durata de viata destul de scurta, in general de doi, maximum trei ani. In randul celor mai populare flori de gradina perene intra si: </p>
    <br>
    <p> &nbsp&nbsp&nbsp&nbsp1)trandafirii;
    </p>
    <p> &nbsp&nbsp&nbsp&nbsp2)narcisele;</p>
    <p> &nbsp&nbsp&nbsp&nbsp3)lalelele;</p>
    <p> &nbsp&nbsp&nbsp&nbsp4)zambilele;</p>
    <p> &nbsp&nbsp&nbsp&nbsp5)garoafele;</p>
    <p> &nbsp&nbsp&nbsp&nbsp6)muscatele;</p>
    <p> &nbsp&nbsp&nbsp&nbsp7)margaretele;</p>
    <p> &nbsp&nbsp&nbsp&nbsp8)lavanda.</p>
    <br>
    <p>Plantele perene presupun, de regula, costuri mai mare de ingrijire decat a plantelor anuale, mai ales ca, in eventualitatea in care se imbolnavesc, tratarea acestora implica un buget financiar si de timp destul de mare. Prin urmare, daca optezi pentru plantarea florilor perene, trebuie sa ai in vedere ca iti iei un angajament pe termen lung, daca vrei sa te bucuri de o gradina cu plante frumoase si sanatoase.</p>
    <br>
    <h2>Capitolul 3</h2>
    <br>
    <h3> Ingrijirea florilor de gradina in functie de anotimp </h3>
    <br>
    <p>Gradina de flori are nevoie de grija ta in permanenta, pentru a-si pastra frumusetea data de plante sanatoase. Ca sa te asiguri ca florile tale vor creste armonios si se vor dezvolta in mod corespunzator, ti-am pregatit un mic ghid de ingrijire in functie de anotimp. Iata ce ai de facut:
    </p>
    <br>
   <p>1) Primavara
<br>Ocupa-te de pamantul gradinii incepand cu luna martie, prin utilizarea compostului, astfel incat acesta sa poata retine umiditatea in anotimpurile calde ce vor urma. Pentru florile sensibile la schimbarile de temperatura ale acestui sezon, foloseste protectii speciale pe care le gasesti in florarii, sau realizeaza unele din sticle de plastic pe care nu le mai folosesti. Nu trebuie decat sa indepartezi partea de jos, dupa ce ai taiat-o cu o foarfeca, si sa amplasezi sticla peste planta, astfel incat aceasta sa fie aparata de intemperii. 
Indeparteaza buruienile crescute dupa ultimul inghet si nu uita sa toaletezi trandafirii si lavanda. Dupa luna aprilie, cand florile de primavara incep sa umple gradina de culoare, ai grija sa indepartezi florile uscate si cazute pe pamant.
    </p>
    <br>
    <br>
    <p>2) Vara 
        <br>Cel mai bun sfat pentru ingrijirea florilor din gradina pe timp de vara este referitor la udarea acestora. Evita mijlocul zilei pentru aceasta activitate, intrucat exista doua posibile consecinte negative – din cauza caldurii, apa se va evapora mult mai repede, iar pamantul nu va avea timp sa o absoarba, iar picaturile de apa de pe frunze si flori vor accelera uscarea acestora, din cauza reflectarii razelor de soare. 
        <br>
        <br>Se recomanda udarea florilor dimineata, pe cat posibil inainte de rasaritul soarelui, sau seara, dupa ce acesta apune.Intrucat in gradina este o explozie de culoare in timpul verii, trebuie sa te ocupi de toate petalele cazute pe pamant. Stabileste, asadar, o seara din saptamana cand poti curata solul de materiile vestejite. De asemenea, este indicata toaletarea plantelor de gradina ce infloresc in acest anotimp, intrucat acest pas le va ajuta sa dezvolte o coroana senzationala.
    </p>
    <br>
    <p>3) Toamna
        <br>In cele mai multe curti, anotimpul ploios este sinonim cu un covor de frunze. Dupa ce cureti intreaga curte, nu arunca aceste frunze uscate, ci pastreaza-le in saci de plastic, in care poti adauga resturi menajere, iar astfel vei obtine propriul tau compost. Il vei folosi in gradina dupa terminarea iernii, pentru a putea pregati pamantul de noua generatie de flori!Serile calde de toamna timpurie pot acomoda petreceri pe terasa gradinii tale, asa ca asigura-te ca instalezi corpuri de iluminat pentru exterior, pe langa mobilierul de gradina din lemn sau fier, astfel incat florile tale sa poata fi admirate chiar si dupa lasarea serii. 
        <br>
        <br>Ultima luna de toamna este momentul propice pentru mutarea florilor la interior, pentru soiurile carora nu le plac temperaturile scazute (begonia, fuchsia, muscata s.a.).
    </p>
    <br>
    <p>4) Iarna
        <br>In anotimpul rece nu ai prea multe de facut in gradina. Zilele scurte si vremea neprietenoasa te vor obliga sa ramai in interior, la caldura, asa ca aceasta este cea mai buna perioada in care poti face cercetarile necesare pentru pregatirea gradinii din sezoanele viitoare. 
        <br>
        <br>Cauta cei mai recomandati comercianti de seminte si bulbi de flori, dar nu uita nici de uneltele pe care le folosesti la gradinarit. Poate unele au nevoie de intretinere sau poate ca este momentul ca achizitionezi unele noi – oricum ar fi, asigura-te ca odata cu venirea primaverii „trusa” ta este completa, pentru a-ti putea ingriji gradina asa cum trebuie.
        <br>
        <br>
        <br>Nu in ultimul rand, protejeaza-ti curtea de daunatorii florilor de gradina, printre care parazitii florilor. Parcurge ghidul nostru pe aceasta tema, ca sa intelegi cum iti poti mentine florile si plantele sanatoase pe tot parcursul anului. 

        O gradina inflorita pe tot parcursul anului, indiferent de anotimp, poate sa para o realizare imposibil de atins. Insa nu e nevoie decat de putina ambitie, de cateva informatii esentiale si de o dragoste pentru tot ce e frumos, iar aceasta poate deveni o realitate chiar in curtea ta! 
        
        Dupa ce ai aplicat cele mai bune idei de amenajari pentru o curte mica, pentru o gradina Feng Shui sau dupa ce ai plantat gazon verde in locul desemnat pentru joaca copiilor, acum e momentul sa te ocupi de gradina de flori, asa ca spor la treaba!
    </p>
